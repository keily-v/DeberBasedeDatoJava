    private void cargarDatos() {
        
        try {
            Connection conexion = conecta.getConnection();
            ps = conexion.prepareStatement("select * from estudiante", ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rs = ps.executeQuery();
            rs.next();
            datosText();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "No existen mas estudiantes");
        }

    }

    private void datosText() {
        try {
            this.txtBuscar.setText(rs.getObject(1).toString());
            this.txtNombre.setText(rs.getObject(2).toString());
            this.cbGenero.setSelectedItem(rs.getObject(4).toString());
            this.cbMateria.setSelectedItem(rs.getObject(5).toString());
            this.txtNota.setText(rs.getObject(3).toString());

        } catch (Exception ex) {
            System.err.println("No existen m√°s datos de estudiantes");
        }
    }


